            f"üë• –í—Å–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π: **{stats['total_users']}**\n"
            f"üö´ –ó–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö: **{stats['banned_users']}**\n"
            f"‚≠ê –í—Å–µ–≥–æ –∏–∑–±—Ä–∞–Ω–Ω—ã—Ö: **{stats['total_favorites']}**\n"
            f"üìù –í—Å–µ–≥–æ –∫–æ–º–∞–Ω–¥: **{stats['total_commands']}**\n"
            f"üé≤ –°–ª—É—á–∞–π–Ω—ã—Ö —Ä–µ—Ü–µ–ø—Ç–æ–≤: **{stats['random_recipe_requests']}**\n"
